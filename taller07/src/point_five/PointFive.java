package point_five;

public class PointFive {
  public PointFive() {
    /*
     * Las excepciones marcadas son aquellas que el compilador conoce y obliga al
     * programador a manejarlas. Estas excepciones se deben declarar en el mismo
     * codigo.
     * Las excepciones sin marcar son aquellas que el compilador no
     * conoce y no obliga al programador a manejarlas. Estas excepciones son
     * runtime error.
     */
    System.out.println("Las excepciones marcadas son aquellas que el compilador conoce y obliga al");
    System.out.println("programador a manejarlas. Estas excepciones se deben declarar en el mismo");
    System.out.println("codigo.");
    System.out.println("Las excepciones sin marcar son aquellas que el compilador no");
    System.out.println("conoce y no obliga al programador a manejarlas. Estas excepciones son");
    System.out.println("runtime error.\n\n");
  }
}
